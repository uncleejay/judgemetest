Cypress.Commands.add('performSearch', (keyword) => {
    // Enter a keyword in the search bar and press Enter
    cy.get('#search-input').type(`${keyword}{enter}`);
    
    // Verify that the results are loaded
    cy.get('.search-results__list').should('be.visible');
  });